
let n1 = 30;
let n2 = 25;


let resulAdi = n1 + n2;
console.log(`Resultado da Adição entre ${n1} e ${n2}: ${resulAdi}`);


let resulSub = n1 - n2;
console.log(`Resultado da Subtração entre ${n1} e ${n2}: ${resulSub}`);


let resulMulti = n1 * n2;
console.log(`Resultado da Multiplicação entre ${n1} e ${n2}: ${resulMulti}`);


let resulDiv = n1 / n2;
console.log(`Resultado da Divisão entre ${n1} e ${n2}: ${resulDiv}`);

